            const mobileOpenMenus = document.querySelectorAll('#mobile-nav .open-menu');
            const searchBtn = document.querySelector('.search-btn');
            const searchClose = document.querySelector('.search-close');
            const searchBox = document.querySelector('.search-box-elements');
            const search = document.getElementById('search');
            const modalDialog = search?.querySelector('.modal-dialog');
            const modalContent = search?.querySelector('.modal-content');
            const closeBtn = search?.querySelector('.modal-close');
            const openBtn = document.getElementById('open-search');
            const customDropdown = document.querySelector('.custom-dropdown');
            const customDropdownSelected = customDropdown?.querySelector('.selected');
            const customDropdownOptions = customDropdown?.querySelector('.options');

            const closeDesktopDropdowns = () => {
              desktopDropdowns.forEach((dropdown) => dropdown.classList.add('hidden'));
              desktopDropdownToggles.forEach((toggle) => {
                toggle.setAttribute('aria-expanded', 'false');
                toggle.querySelector('.icon')?.classList.remove('rotate-180');
              });
            };

            const handleHeaderState = () => {
              if (header) {
                const shouldStick = window.innerWidth > 1024 && window.scrollY > 300;
                header.classList.toggle('sticky', shouldStick);
                if (headerHeightElement) {
                  headerHeightElement.style.height = shouldStick ? '59px' : '0';
                }
              }

              if (mobNav) {
                mobNav.classList.toggle('sticky', window.scrollY > 100);
              }
            };

            const activateTab = (tabId) => {
              tabItems.forEach((item) => {
                item.classList.toggle('active', item.getAttribute('data-tab') === tabId);
              });

              tabContents.forEach((content) => {
                const isActive = content.id === `tab-${tabId}`;
                content.classList.toggle('active', isActive);
                content.classList.toggle('hidden', !isActive);
              });
            };

            const setMobileMenuState = (isOpen) => {
              if (!mobileNavList || !menuButton) {
                return;
              }

              mobileNavList.classList.toggle('open', isOpen);
              menuButton.setAttribute('aria-expanded', String(isOpen));
              document.body.classList.toggle('nav-open', isOpen);
            };

            desktopDropdownToggles.forEach((toggle) => {
              toggle.addEventListener('click', (event) => {
                event.preventDefault();
                event.stopPropagation();

                const dropdown = toggle.nextElementSibling;
                if (!dropdown) {
                  return;
                }

                const shouldOpen = dropdown.classList.contains('hidden');
                closeDesktopDropdowns();

                if (shouldOpen) {
                  dropdown.classList.remove('hidden');
                  toggle.setAttribute('aria-expanded', 'true');
                  toggle.querySelector('.icon')?.classList.add('rotate-180');
                }
              });
            });

            tabItems.forEach((item) => {
              item.addEventListener('click', (event) => {
                event.preventDefault();
                event.stopPropagation();
                activateTab(item.getAttribute('data-tab'));
              });
            });

            if (tabItems.length > 0) {
              activateTab(tabItems[0].getAttribute('data-tab'));
            }

            if (menuButton && mobileNavList) {
              menuButton.addEventListener('click', (event) => {
                event.preventDefault();
                setMobileMenuState(!mobileNavList.classList.contains('open'));
              });
            }

            mobileOpenMenus.forEach((button) => {
              button.addEventListener('click', (event) => {
                event.preventDefault();
                event.stopPropagation();

                const submenu = button.nextElementSibling;
                if (!submenu || submenu.tagName.toLowerCase() !== 'ul') {
                  return;
                }

                const isOpen = submenu.style.maxHeight && submenu.style.maxHeight !== '0px';
                submenu.style.maxHeight = isOpen ? '0' : `${submenu.scrollHeight}px`;
                button.classList.toggle('rotate', !isOpen);
              });
            });

            if (searchBtn && searchBox) {
              searchBtn.addEventListener('click', () => {
                searchBox.classList.toggle('hidden');
              });
            }

            if (searchClose && searchBox) {
              searchClose.addEventListener('click', (event) => {
                event.preventDefault();
                searchBox.classList.add('hidden');
              });
            }

            if (openBtn && modalDialog) {
              openBtn.addEventListener('click', () => {
                modalDialog.classList.add('is-active');
              });
            }

            if (closeBtn && modalDialog) {
              closeBtn.addEventListener('click', (event) => {
                event.preventDefault();
                modalDialog.classList.remove('is-active');
              });
            }

            if (customDropdown && customDropdownSelected && customDropdownOptions) {
              customDropdown.addEventListener('click', () => {
                customDropdown.classList.toggle('active');
              });

              customDropdownOptions.addEventListener('click', (event) => {
                const option = event.target.closest('li');
                if (!option) {
                  return;
                }

                customDropdownSelected.textContent = option.textContent;
                customDropdown.classList.remove('active');
              });
            }

            document.addEventListener('mousedown', (event) => {
              if (!event.target.closest('.dropdown') && !event.target.closest('.dropdown-toggle')) {
                closeDesktopDropdowns();
              }

              if (
                modalDialog?.classList.contains('is-active') &&
                modalContent &&
                !modalContent.contains(event.target) &&
                !openBtn?.contains(event.target)
              ) {
                modalDialog.classList.remove('is-active');
              }

              if (customDropdown && !customDropdown.contains(event.target)) {
                customDropdown.classList.remove('active');
              }
            });

            window.addEventListener('scroll', handleHeaderState);
            window.addEventListener('resize', handleHeaderState);
            handleHeaderState();
          });
        </script>
